    public List<Integer> sorteio(){
        List<Integer> lista = new ArrayList<Integer>();
        Integer valor;
        while(lista.size() < 5) {
            valor = 1 + (int) (Math.random() * 5);
            // se o número for distinto
            if(!lista.contains(valor)) {
                    lista.add(valor);
            }
        }
      return lista;
    }

