    private void verificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verificarActionPerformed
        // TODO add your handling code here:
        if(CB1.isSelected()==true||CB2.isSelected()==true||CB3.isSelected()==true||CB4.isSelected()==true||CB5.isSelected()==true){
           int uu=0;
           if(CB1.isSelected()==true){
               uu=1;
           }
           if(CB2.isSelected()==true){
               uu=2;
           }
           if(CB3.isSelected()==true){
               uu=3;
           }
           if(CB4.isSelected()==true){
               uu=4;
           }
           if(CB5.isSelected()==true){
               uu=5;
           }
           if(uu==numero){
               resp=true;
           }if(uu!=numero){
               resp=false;
           }
            resposta_da_questão respo = new resposta_da_questão(nume,resp);
            respo.setLocationRelativeTo(null);
            respo.setVisible(true); 
            dispose();
        }else{
            JOptionPane.showMessageDialog(null,"marque uma resposta");
        }
        
    }//GEN-LAST:event_verificarActionPerformed

